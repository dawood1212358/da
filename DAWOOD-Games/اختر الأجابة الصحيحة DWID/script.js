// إعادة تعيين اللعبة
function resetGame() {
  teams[0].score = 0;
  teams[1].score = 0;
  usedQuestions = [];
  currentTeam = 0;
  updateScores();
}

// الانتقال إلى السؤال التالي
function nextQuestion() {
  if (teams[0].score >= 500 || teams[1].score >= 500 || usedQuestions.length === questions.length) {
    endGame();
    return;
  }

  let qIndex;
  do {
    qIndex = Math.floor(Math.random() * questions.length); // اختيار سؤال عشوائي
  } while (usedQuestions.includes(qIndex) && usedQuestions.length < questions.length); // التحقق من عدم تكرار السؤال
  usedQuestions.push(qIndex);
  currentQuestionIndex = qIndex;

  const q = questions[qIndex];
  const shuffledOptions = shuffleOptions(q.options, q.answer); // خلط الخيارات عشوائيًا
  document.getElementById('questionText').innerText = q.question;
  shuffledOptions.forEach((opt, i) => {
    const btn = document.getElementById('opt' + i);
    btn.innerText = opt.option;
    btn.disabled = false;
    btn.style.background = "#222b";
    btn.style.color = "#fff";
    btn.onclick = () => answer(i, q.answer, shuffledOptions);
  });

  document.getElementById('currentTeam').innerText = teams[currentTeam].name;
  startTimer();
}

// خلط الخيارات عشوائيًا
function shuffleOptions(options, correctAnswer) {
  const shuffled = options.map((option, index) => ({
    option,
    index
  }));

  shuffled.sort(() => Math.random() - 0.5); // خلط عشوائي
  shuffled.forEach((item, i) => {
    if (item.index === correctAnswer) {
      item.isCorrect = true; // تعيين الإجابة الصحيحة
    } else {
      item.isCorrect = false;
    }
  });

  return shuffled;
}

// بدء المؤقت
function startTimer() {
  timer = 30;
  updateTimerUI(timer);
  clearInterval(timerInterval);
  ticking = true;

  sounds.tick.pause();
  sounds.tick.currentTime = 0;

  timerInterval = setInterval(() => {
    if (paused) {
      sounds.tick.pause();
      return;
    }

    timer--;
    updateTimerUI(timer);

    if (timer > 0 && ticking) {
      sounds.tick.pause();
      sounds.tick.currentTime = 0;
      sounds.tick.play();
    } else {
      sounds.tick.pause();
      ticking = false;
    }

    if (timer === 0) {
      clearInterval(timerInterval);
      ticking = false;
      sounds.tick.pause();
      disableOptions();
      setTimeout(switchTeam, 700);
    }
  }, 1000);
}

function updateTimerUI(timeLeft) {
  document.getElementById('timerText').textContent = timeLeft;
  const circle = document.getElementById('timerArc');
  const fullDash = 2 * Math.PI * 54;
  let progress = timeLeft / 30;
  circle.setAttribute('stroke-dasharray', fullDash);
  circle.setAttribute('stroke-dashoffset', fullDash * (1 - progress));
  circle.setAttribute('stroke', timeLeft > 5 ? "#1e90ff" : "#e74c3c");
}

function answer(selected, correctAnswer, shuffledOptions) {
  clearInterval(timerInterval);
  ticking = false;
  sounds.tick.pause();
  disableOptions();
  
  if (selected === shuffledOptions.findIndex(opt => opt.isCorrect)) {
    teams[currentTeam].score += 10;
    sounds.correct.currentTime = 0;
    sounds.correct.play();
    highlightOption(selected, true);
  } else {
    sounds.wrong.currentTime = 0;
    sounds.wrong.play();
    highlightOption(selected, false);
    highlightOption(shuffledOptions.findIndex(opt => opt.isCorrect), true);
  }
  
  updateScores();
  setTimeout(switchTeam, 900);
}

function disableOptions() {
  for (let i = 0; i < 4; i++) {
    document.getElementById('opt' + i).disabled = true;
  }
}

function highlightOption(idx, correct) {
  const btn = document.getElementById('opt' + idx);
  btn.style.background = correct ? "#27ae60" : "#c0392b";
  btn.style.color = "#fff";
}

function updateScores() {
  document.getElementById('score1g').innerText = teams[0].score;
  document.getElementById('score2g').innerText = teams[1].score;
}

function switchTeam() {
  currentTeam = 1 - currentTeam;
  nextQuestion();
}

function endGame() {
  document.getElementById('gameScreen').style.display = 'none';
  document.getElementById('winnerScreen').style.display = 'flex';
  let winner, loser;
  if (teams[0].score > teams[1].score) {
    winner = teams[0].name;
    loser = teams[1].name;
  } else if (teams[1].score > teams[0].score) {
    winner = teams[1].name;
    loser = teams[0].name;
  } else {
    winner = "تعادل";
    loser = "تعادل";
  }
  document.getElementById('winnerTeam').innerText = winner;
  document.getElementById('loserTeam').innerText = loser;
  sounds.winner.currentTime = 0;
  sounds.winner.play();
  setTimeout(showEndVideo, 10000);
}

function showEndVideo() {
  document.getElementById('winnerScreen').style.display = 'none';
  const endVideo = document.getElementById('endVideo');
  endVideo.style.display = 'block';
  endVideo.play();
  endVideo.onended = function() {
    endVideo.style.display = 'none';
    document.getElementById('mainScreen').style.display = 'flex';
  };
}

document.getElementById('pauseBtn').onclick = function() {
  paused = !paused;
  if (paused) {
    clearInterval(timerInterval);
    sounds.tick.pause();
    this.innerText = "▶️";
  } else {
    this.innerText = "⏸️";
    startTimer();
  }
};
