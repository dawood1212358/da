let usedQuestions = [];
let current = 0;
let score1 = 0;
let score2 = 0;
let currentTeam = 1;
let timer = 10;
let interval;
let isPaused = false;
let gameFinished = false;
let team1Name = "الكواكب";
let team2Name = "النجوم";
let countdownAudio;

function startGame() {
  document.getElementById("startScreen").style.display = "none";
  document.getElementById("gameScreen").style.display = "block";
  loadQuestion();
}

function loadQuestion() {
  if (gameFinished || usedQuestions.length === questions.length) {
    endGame();
    return;
  }

  let index;
  do {
    index = Math.floor(Math.random() * questions.length);
  } while (usedQuestions.includes(index));

  current = index;
  usedQuestions.push(index);

  document.getElementById('question').innerText = questions[current].q;
  document.getElementById('currentTeam').textContent = currentTeam === 1 ? team1Name : team2Name;
  document.getElementById('feedback').innerText = '';
  startTimer();
}

function startTimer() {
  clearInterval(interval);
  timer = 10;
  document.getElementById('timer').textContent = timer;
  
  if (countdownAudio) {
    countdownAudio.pause();
    countdownAudio.currentTime = 0;
  }
  countdownAudio = new Audio('sounds/countdown.mp3');
  countdownAudio.play();

  interval = setInterval(() => {
    if (!isPaused && !gameFinished) {
      timer--;
      document.getElementById('timer').textContent = timer;
      
      if (timer <= 0) {
        clearInterval(interval);
        document.getElementById('feedback').innerText = "⏱ انتهى الوقت!";
        setTimeout(nextQuestion, 1500);
      }
    }
  }, 1000);
}

function togglePause() {
  isPaused = !isPaused;
  if (countdownAudio) {
    isPaused ? countdownAudio.pause() : countdownAudio.play();
  }
}

function submitAnswer(ans) {
  if (gameFinished) return;
  
  clearInterval(interval);
  const correct = questions[current].a;
  const isRight = ans === correct;
  const feedback = document.getElementById('feedback');
  
  const sound = new Audio(isRight ? "sounds/correct.mp3" : "sounds/wrong.mp3");
  sound.play();

  if (isRight) {
    feedback.innerText = "🎉 إجابة صحيحة!";
    currentTeam === 1 ? score1++ : score2++;
  } else {
    feedback.innerText = "😅 إجابة خاطئة!";
  }

  updateScores();
  
  if (score1 >= 100 || score2 >= 100) {
    endGame();
  } else {
    setTimeout(nextQuestion, 2000);
  }
}

function updateScores() {
  document.getElementById('score1').textContent = score1;
  document.getElementById('score2').textContent = score2;
}

function nextQuestion() {
  currentTeam = currentTeam === 1 ? 2 : 1;
  loadQuestion();
}

function endGame() {
  gameFinished = true;
  clearInterval(interval);
  if (countdownAudio) {
    countdownAudio.pause();
    countdownAudio.currentTime = 0;
  }
  
  const winner = score1 > score2 ? team1Name : team2Name;
  document.getElementById('winnerMessage').textContent = `🎉 الفريق الفائز هو: ${winner}!`;
  document.getElementById("endScreen").style.display = "block";
  document.getElementById("gameScreen").style.display = "none";
  document.getElementById("winSound").play();
}